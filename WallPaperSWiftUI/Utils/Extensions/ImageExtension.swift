    func resizeFitTo(size: CGFloat,renderingMode: TemplateRenderingMode = .original ,radius: CGFloat = 0) -> some View {
        self.resizable()
            .renderingMode(renderingMode)
            .scaledToFit()
            .frame(width: size,height: size)
            .clipped()
            .contentShape(Rectangle())
            .cornerRadius(radius)
    }
    
